    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String batchComboResult = batchCombo.getSelectedItem().toString();
        String feesComboResult = feesCombo.getSelectedItem().toString();
        if(nameField.getText().equals("") || departmentField.getText().equals("") || phoneField.getText().equals("")
                || dateOfAdmissionField.getDate() == null || addressField.getText().equals("")
                || batchComboResult.equals("Select Fees") || feesComboResult.equals("Select Fees")){
            
            
             JOptionPane.showMessageDialog(null, "Please fill all fields", "Error", 0);
           
            
        }else{
        
            String name = nameField.getText().toString();
            String dept = departmentField.getText().toString();
            String ph = phoneField.getText().toString();
            Date date = dateOfAdmissionField.getDate();
            
            String batchName = batchCombo.getSelectedItem().toString();
            Integer batchId = batchDAOImpl.getbatchIdByName(batchName);
            
            Double feesAmount = Double.parseDouble(feesCombo.getSelectedItem().toString());
            Integer feesId = feesDAOImpl.getfeesIdByAmount(feesAmount);
        
            String address = addressField.getText();
            
            Batch b = batchDAOImpl.getBatchById(batchId);
            Fees f = feesDAOImpl.getFeesById(feesId);
            
            Student s = new Student();
            s.setStudentName(name);
            s.setDepartment(dept);
            s.setPhoneNumber(ph);
            s.setDate_of_admission(date);
            s.setAddress(address);
            s.setBatch(b);
            s.setFees(f);
            s.setCreatedDate(todayDate);
            s.setCreatedBy(u.getUserName());
            
            int result = studentDAOImpl.addStudent(s);
            
            if(result > 0){
                JOptionPane.showMessageDialog(null, s.getStudentName()+" added successfully!");
                clearFields();
                fillTable();
            }else{
                JOptionPane.showMessageDialog(null, null, "Error", 0);
            
            }
            
        
        }
    }//GEN-LAST:event_addButtonActionPerformed

