    public void fillTable(){
    
        Object columns[]={"ID","Name","Department","Phone #","Date of Admission","Batch","Fees","Address","Created Date","Created By","Modified Date","Modified By"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        List<Student> allStudents = studentDAOImpl.getAllStudent();
        
        for(Student s:allStudents){
            Batch b = batchDAOImpl.getBatchById(s.getBatch().getBatchId());
            Fees f = feesDAOImpl.getFeesById(s.getFees().getFeesId());
            
            Object rowData[] = {s.getStudentId(),s.getStudentName(),s.getDepartment(),s.getPhoneNumber(),s.getDate_of_admission().toString(),
                b.getBatchName(),f.getFeesAmount(),s.getAddress(),s.getCreatedDate(),s.getCreatedBy(),s.getModifiedDate(),s.getModifiedBy()};
            
            model.addRow(rowData);
            studentTable.setModel(model);
        
        }
    }
    
    
    
